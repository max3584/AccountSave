package data.columns;

public class SiteAccountCount {

	private String name;
	private int site_account_count;
	
	public SiteAccountCount() {}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getSite_account_count() {
		return site_account_count;
	}

	public void setSite_account_count(int site_account_count) {
		this.site_account_count = site_account_count;
	}
}
