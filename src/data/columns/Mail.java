package data.columns;

public class Mail {

	private String mail;
	private int mail_count;
	
	public Mail() {}

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public int getMail_count() {
		return mail_count;
	}

	public void setMail_count(int mail_count) {
		this.mail_count = mail_count;
	}
	
}
